#include<stdio.h>
int main() {
    //int
    int firstNumber = 10;
    printf("my number is  %d\n" , firstNumber);
    //float
    float floatNumber = 10.32;
    printf("my number is  %lf\n" , floatNumber);
    //double
    double doubleNumber = 8.25;
    printf("my number is  %f\n" , doubleNumber);
    //string
    char name = 'H';
    printf("my character is  %c\n" , name);


    return 0;

}
